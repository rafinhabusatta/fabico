            <h1 class="title">A Fabico</h1>
            <p>A Faculdade de Biblioteconomia e Comunicação foi criada pela Portaria nº 714, assinada pelo Reitor Eduardo Zaccaro Faraco, em 1º de setembro de 1970, em sequência à Reforma Universitária implantada pela Lei nº 5540 de 1968.</p>
            <p>Estão na sua origem o Curso de Graduação em Jornalismo, criado em 1952, vinculado à antiga Faculdade de Filosofia, e a Escola de Biblioteconomia e Documentação, formada a partir do curso técnico de Biblioteconomia, de 1947, agregado à Faculdade de Ciências Econômicas e aprovado como curso superior em 1958.</p>
            <p>Com a criação da FABICO, o antigo Jornalismo é reformulado, tornando-se uma das habilitações do novo Curso de Comunicação Social. Em agosto de 1970 é feita a mudança para o prédio da Gráfica da Universidade, no Campus Saúde, onde passa a ocupar o terceiro andar, com salas de aula, sala de redação, laboratório fotográfico, Chefia do Departamento de Comunicação e Direção da nova Unidade.</p>
            <p>Em 1972, o Curso de Biblioteconomia e Documentação e a Biblioteca da Faculdade instalam-se no prédio, ocupando o quarto andar. Nesta época são estabelecidas as habilitações unificadas do Curso de Comunicação Social – Jornalismo Gráfico e Audiovisual, Relações Públicas e Publicidade e Propaganda.</p>
            <p>Em 1985 o currículo do Curso de Comunicação é reformulado e são estabelecidas as habilitações em Jornalismo, em Publicidade e Propaganda e em Relações Públicas para a formação de Bacharéis em Comunicação.</p>
            <p>O Programa de Pós-Graduação em Comunicação e Informação - PPGCOM é aprovado e implantado em 1996, em nível de Mestrado, sendo ampliado em 2000 com a implantação do Doutorado.</p>
            <p>Na área de Ciências da Informação são criados o Curso de Graduação em Arquivologia, no ano de 1999, e o Curso de Museologia, em 2008.</p>
            <p>Desde 2006, com a transferência da Gráfica da Universidade para Rua Ramiro Barcelos, 2500, a FABICO passou a ocupar todo o prédio e parte do Anexo 1 do Campus Saúde. Abriga 1.532 alunos, 71 professores, 40 técnico-administrativos e equipe terceirizada.</p>
